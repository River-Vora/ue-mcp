// End-to-end verification of the feedback OAuth device flow.
// Uses dist/auth.js + raw GitHub API to: run device flow, fetch user, post a
// test issue, immediately delete it. No-op if a token is already cached.
//
// Run from repo root:  node scripts/verify-oauth.mjs

import {
  resolveUserAuth,
  readUserAuth,
  tryExchangeDeviceCode,
} from "../dist/auth.js";

const REPO = "db-lyon/ue-mcp";

async function poll() {
  for (;;) {
    const state = await resolveUserAuth();
    if (state.kind === "auth") return state.auth;

    console.log("\nDevice flow ready.");
    console.log("  URL : ", state.pending.verification_uri);
    console.log("  CODE: ", state.pending.user_code);
    console.log(
      `  Expires in ${Math.round((state.pending.expires_at - Date.now() / 1000) / 60)} min`,
    );
    console.log("Authorize, then we'll poll every 5s.\n");

    while (true) {
      await new Promise((r) => setTimeout(r, state.pending.interval * 1000));
      const result = await tryExchangeDeviceCode(state.pending);
      if (result.kind === "auth") {
        console.log("Token acquired.");
        return result.auth;
      }
      if (result.kind === "expired") {
        console.log("Code expired. Restarting device flow...");
        break;
      }
      if (result.kind === "denied") {
        throw new Error("User denied authorization.");
      }
      process.stdout.write(".");
    }
  }
}

async function main() {
  const cached = await readUserAuth();
  if (cached) {
    console.log(`Already authed as @${cached.login}. Token will be reused.`);
  }

  const auth = await poll();
  console.log(`\nLogged in as: @${auth.login}`);

  // Post a test issue
  const title = `[verify-oauth] device flow smoke test ${new Date().toISOString()}`;
  const body =
    "Auto-generated by `scripts/verify-oauth.mjs` to confirm the OAuth path " +
    "authors issues as the real user. Closing immediately.";

  console.log("\nPosting test issue...");
  const postRes = await fetch(`https://api.github.com/repos/${REPO}/issues`, {
    method: "POST",
    headers: {
      Authorization: `token ${auth.token}`,
      Accept: "application/vnd.github+json",
      "Content-Type": "application/json",
      "User-Agent": "ue-mcp-verify",
    },
    body: JSON.stringify({ title, body, labels: ["agent-feedback"] }),
  });
  if (!postRes.ok) {
    throw new Error(`Issue create failed: ${postRes.status} ${await postRes.text()}`);
  }
  const issue = await postRes.json();
  const author = issue.user?.login;
  console.log(`  Issue #${issue.number} created`);
  console.log(`  URL    : ${issue.html_url}`);
  console.log(`  Author : @${author}`);

  if (author !== auth.login) {
    console.error(
      `\nFAIL: issue author (@${author}) does not match auth user (@${auth.login}).`,
    );
    process.exit(1);
  }

  console.log("\nClosing + deleting test issue...");
  // Close, then transfer-or-delete. Plain DELETE on issues isn't a public API;
  // close it and apply a self-cleanup label that the placeholder workflow eats.
  const closeRes = await fetch(
    `https://api.github.com/repos/${REPO}/issues/${issue.number}`,
    {
      method: "PATCH",
      headers: {
        Authorization: `token ${auth.token}`,
        Accept: "application/vnd.github+json",
        "Content-Type": "application/json",
        "User-Agent": "ue-mcp-verify",
      },
      body: JSON.stringify({ state: "closed", state_reason: "not_planned" }),
    },
  );
  if (!closeRes.ok) {
    console.error(`Close failed: ${closeRes.status} ${await closeRes.text()}`);
  } else {
    console.log("  Closed.");
  }

  console.log(`\nVERIFIED: feedback OAuth path authors issues as @${auth.login}.`);
}

main().catch((e) => {
  console.error("FAILED:", e.message);
  process.exit(1);
});
